function alterarTema() {
  document.body.classList.toggle("dark");
  //   body: retoma os conteúdos da tag <body> do HTML
  //   classList: atribui uma classe ao conteúdo da tag <body> que poderá ser retomada no CSS
  //   toggle: remete à alternância do botão "Alterar Tema"
  //   ("dark"): é a segunda classe que estará na alternância
}